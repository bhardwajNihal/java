package Hashmaps;


// custom hashmap implementation
// takes generic key, value pairs
public class CustomHasmap <K, V> {
    
    K key;
    V value;

    CustomHasmap(){
        this.key = null;
        this.value = null;
    }

    CustomHasmap(K key, V value){
        this.key = key;
        this.value = value;
    }

    


}
